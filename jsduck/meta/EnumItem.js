/**
 * @class meta.EnumItem
 *
 * 用于{@link Enum}中的每一项的数据结构
 */
function EnumItem() {
    /**
     * @property {number} value
     *
     * 当前项的数值
     */
    this.value;

    /**
     * @property {string} alias
     *
     * 当前项的别名
     */
    this.alias;

    /**
     * @property {string} text
     *
     * 当前项的文字表达
     */
    this.text;
}
